            <h2>Guidelines for Hypertension</h2>
            <p>Hypertension, or high blood pressure, affects about <strong>1 in 3 adults</strong> in the United States. If left untreated, it can lead to serious health issues such as heart disease and stroke.</p>
            <h3>Symptoms of Hypertension</h3>
            <p>Hypertension often has no noticeable symptoms. However, some people may experience:</p>
            <ul>
                <li>Headaches</li>
                <li>Shortness of breath</li>
                <li>Nosebleeds</li>
                <li>Flushing</li>
                <li>Visual changes</li>
            </ul>
            <h3>Management Guidelines</h3>
            <p>Here are some guidelines to manage hypertension:</p>
            <ul>
                <li>Monitor your blood pressure regularly.</li>
                <li>Maintain a healthy weight through diet and exercise.</li>
                <li>Follow a balanced diet rich in fruits, vegetables, whole grains, and low-fat dairy products (DASH diet).</li>
                <li>Limit your salt intake to less than <strong>2,300 mg per day</strong>.</li>
                <li>Engage in regular physical activity (at least <strong>150 minutes</strong> of moderate exercise weekly).</li>
                <li>Avoid excessive alcohol consumption.</li>
                <li>Manage stress through relaxation techniques, such as yoga or meditation.</li>
                <li>Follow your doctor's advice regarding medications.</li>
            </ul>
            <h3>Prevention Tips</h3>
            <ul>
                <li>Eat a heart-healthy diet.</li>
                <li>Stay physically active.</li>
                <li>Maintain a healthy weight.</li>
                <li>Limit alcohol consumption.</li>
                <li>Avoid tobacco products.</li>
            </ul>
        </section>

        <section class="diabetes">
            <h2>Guidelines for Diabetes</h2>
            <p>Diabetes is a chronic condition affecting over <strong>463 million people worldwide</strong>. It occurs when the body cannot properly regulate blood sugar levels.</p>
            <h3>Symptoms of Diabetes</h3>
            <p>Common symptoms of diabetes include:</p>
            <ul>
                <li>Increased thirst and frequent urination</li>
                <li>Extreme fatigue</li>
                <li>Blurred vision</li>
                <li>Slow-healing sores</li>
                <li>Unexplained weight loss</li>
            </ul>
            <h3>Management Guidelines</h3>
            <p>Here are some guidelines to manage diabetes:</p>
            <ul>
                <li>Monitor your blood sugar levels regularly.</li>
                <li>Follow a balanced meal plan with appropriate portion sizes.</li>
                <li>Incorporate more whole grains, fruits, and vegetables into your diet.</li>
                <li>Limit sugary foods and drinks.</li>
                <li>Engage in regular physical activity (at least <strong>150 minutes</strong> of moderate exercise weekly).</li>
                <li>Take medications as prescribed by your healthcare provider.</li>
                <li>Manage stress through relaxation techniques.</li>
            </ul>
            <h3>Prevention Tips</h3>
            <ul>
                <li>Maintain a healthy weight.</li>
                <li>Be physically active.</li>
                <li>Eat a balanced diet low in refined sugars and high in fiber.</li>
                <li>Stay hydrated and limit sugary beverages.</li>
                <li>Regularly monitor your health and consult with your doctor.</li>
            </ul>
        </section>

