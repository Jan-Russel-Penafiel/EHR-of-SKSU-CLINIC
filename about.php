Dr. Nelson T. Binag, the first President of SKPSC, served for 18 years and led an intensified faculty and staff development program. He initiated extension programs in various municipalities, producing many Master's and Doctor's degree holders. SKSU offers programs and special courses that help develop locally homegrown industries using indigenous resources and raw materials.

The institution has been rated Level III-A in the SUC leveling and has established alliances with international organizations and institutions. Dr. Teresita L. Cambel pursued the dream of universityhood, and Republic Act No.9966 was enacted in 2010, converting the former SKSU into SKSU.

Dr. Rolando F. Hechanova, the 3rd President of SKSU, took office in 2014 and has spurred various developments and initiatives, including infrastructure improvements, faculty promotions, and more accredited programs. SKSU's reputation as a provider of quality and affordable education is evident in its growth and development.</p>
